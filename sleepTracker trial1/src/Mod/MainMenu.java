    //checks if the current sleep button is pressed, if so, information regarding times that sound was detected and the
    //number of times it was detected are passed into the next screen and the current sleep screen is displayed.
    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {                                         
    	CurrentSleepSAcreen o = new CurrentSleepSAcreen();
    	String strForTimes = "These sounds took place at hours: ";
    	o.setUser(myUser);
    	 setVisible(false);
    	 o.setVisible(true);
    	 o.setTimesWoeknUp(timesWokenUp);
    	 for(int i =0; i<times.size(); i++) {
    		 if(i == times.size()-1) {
    			 strForTimes += times.get(i);
    			 strForTimes += ":";
    			 strForTimes += timesMin.get(i);
    		
    		 }
    		 else if(i == times.size()-2) {
    			 strForTimes += times.get(i);
    			 strForTimes += ":";
    			 strForTimes += timesMin.get(i);
    			 strForTimes += " and ";
    		 }
    		 else {
    			 strForTimes += times.get(i);
    			 strForTimes += ":";
    			 strForTimes += timesMin.get(i);
    			 strForTimes += ", ";
    		 }
    	 }
    	 o.jTextArea1.setText("You made Noise or woke up " + timesWokenUp + " times this night" + "\n" + "This is apporximately " + ((double)timesWokenUp)/6*100 + "% the ammount of times that the average American Wakes up at night" + "\n" + "\n" + strForTimes + " of the day");
    	 
 		o.isVisible();
    }                                        

    
