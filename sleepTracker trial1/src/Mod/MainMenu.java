    //Checks if the analytics screen button is pressed, if so, information about pre-existing conditions is transfered and
    //and the user is taken to the analytics screen
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        analyticsScreen o = new analyticsScreen();
        int hours  = myUser.retConditionTimeAdd();
        o.setUser(myUser);
        setVisible(false);
        o.jTextArea1.setText("");
        if(hours > 5) {
        	hours = 5;
        }
        o.jTextArea1.setText("You stated to suffer from the following conditions: " + myUser.retConditionNames() + "The following tips may benefit you in your sleep: "+ "\n" + myUser.retConditionTips() + "\n" +"\n" + "Your conditions may affect the ammount of sleep you get, consider increasing your sleep by "+hours + " hours.");
        if(myUser.isObese()) {
        	o.jTextArea1.setText("You stated to suffer from the following conditions: " + myUser.retConditionNames() + "The following tips may benefit you in your sleep: "+ "\n" + myUser.retConditionTips() + "\n" + "You may also suffer from sleep disorders due to weight according to your calculated BMI. Ask your doctor for more treatments and information" +"\n"+"\n" + "Your conditions may affect the ammount of sleep you get, consider increasing your sleep by "+hours + " hours.");
        }
		o.setVisible(true);
		o.isVisible();
    }                                        

